def add(a, b):
    """
    Función que suma dos números y devuelve el resultado
    
    Args:
        a: Primer número
        b: Segundo número
        
    Returns:
        La suma de a y b
    """
    return a + b

if __name__ == "__main__":
    # Ejemplo de uso
    print(add(5, 3))  # Imprime 8